package com.quest.practice_vars;

public class TypeCasting {
    public static void main(String[] args) {
        int n=5;
        double d=n;
        System.out.println(d);

        double d2=8.8;
        int i=(int)d2;
        System.out.println(i);

        int b=200;
        byte c=(byte)b;
        System.out.println(c);



    }


}
